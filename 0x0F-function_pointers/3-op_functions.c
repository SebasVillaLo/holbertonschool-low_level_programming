#include <stdio.h>
#include "3-calc.h"
/**
* op_add - estructura para la operacion
* @a: numero
* @b: numero
* Return: Devuelve la operacion
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - estructura para la operacion
* @a: numero
* @b: numero
* Return: Devuelve la operacion
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mod - estructura para la operacion
* @a: numero
* @b: numero
* Return: Devuelve la operacion
*/
int op_mod(int a, int b)
{
	return (a % b);
}
/**
* op_div - estructura para la operacion
* @a: numero
* @b: numero
* Return: Devuelve la operacion
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mul - estructura para la operacion
* @a: numero
* @b: numero
* Return: Devuelve la operacion
*/
int op_mul(int a, int b)
{
	return (a * b);
}
